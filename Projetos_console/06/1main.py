# declaração de variaveis 
x = 5
y = 2

# operação
soma = x + y
subtracao = x - y
multiplicacao = x * y
divisao = x / y
resto = x%y
potencia = x**y

# saida de dados
print("Soma de {} e {} é {}. ".format(x, y, soma)) 
#quarta forma de contatenar 
print(f"A subtração de {x} e {y} é {subtracao}.")
print(f"A multiplicação de {x} e {y} é {multiplicacao}.")
print(f"A divisão de {x} e {y} é {divisao}.")
print(f"A resto da divisão de {x} e {y} é {resto}.")
print(f"{x} elevado a {y} é {potencia}.")

